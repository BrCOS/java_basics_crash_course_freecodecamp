public class Main {
    public static void main(String[] args) {
        int year = 2020;

        if (year == 2019) {
            System.out.println("The year is " + year);
        } else if (year == 2020) {
            System.out.println("The year is " + year);
        } else if (year == 2022) {
            System.out.println("The year is " + year);
        } else {
            System.out.println("All conditions failed");
        }

        //we can add as much else if's as we want
    }
}