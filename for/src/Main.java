public class Main {
    public static void main(String[] args) {
        for (int i = 0; i <= 10; i++) {//inside the parenthesis we declare a variable, then a condition and then a postfix incrementation
            //it will execute the code 10 times, and when the incrementation hit the number 11 (in this case) the condition will be false and the loop will be ended
            System.out.println("i = " + i);
        }
    }
}